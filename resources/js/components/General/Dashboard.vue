<script>
    import FooterComponent from "./FooterComponent.vue";
    import HeaderComponent from "./HeaderComponent.vue";
    import ShippingComponent from "./ShippingComponent.vue";
    import OrderComponent from "./Orders.vue";
    import Wishlist from "./WishlistComponent.vue";
    export default {
        components: {
            HeaderComponent,
            FooterComponent,
            ShippingComponent,
            OrderComponent,
            Wishlist,
        },
        data() {
            return {
                ordersView: true,
                profileView: false,
                shippingDetView: false,
                wishlistView: false,
                isInTransitView: false,
                isCompletedView: false,
                isProcessingView: false,
                isOrdersView: true,
            };
        },
        mounted() {
            this.loadInView();
        },
        methods: {
            checkIfUser() {
                if (this.$store.state.user == null) {
                    window.location.replace("/login");
                }
            },

            loadInView() {
                let path = new URLSearchParams(window.location.search);
                if (path.has("orders")) {
                    this.viewOrders();
                    this.viewAllOrders();
                }
                if (path.has("address")) {
                    this.viewShippingDet();
                }
                if (path.has("wishlist")) {
                    this.viewWishes();
                }
                if (path.has("ordersinprocessing")) {
                    this.viewProcessing();
                }
                if (path.has("ordersintransit")) {
                    this.viewInTransit();
                }
                if (path.has("orderscompleted")) {
                    this.viewCompleted();
                }
                if (path.has("profile")) {
                    this.viewProfile();
                }
            },
            viewOrders() {
                this.ordersView = true;
                this.profileView = false;
                this.shippingDetView = false;
                this.windowHistoryToDashboard();
            },
            viewProfile() {
                this.profileView = true;
                this.ordersView = false;
                this.shippingDetView = false;
                this.windowHistoryToDashboard();
            },
            viewShippingDet() {
                this.shippingDetView = true;
                this.ordersView = false;
                this.profileView = false;
                // this.windowHistoryToDashboard();
            },
            viewWishes() {
                this.wishlistView = true;
                this.ordersView = false;
                this.profileView = false;
                this.shippingDetView = false;
                // this.windowHistoryToDashboard();
            },
            viewAllOrders() {
                this.isOrdersView = true;
                this.isCompletedView = false;
                this.isProcessingView = false;
                this.isInTransitView = false;
            },
            viewCompleted() {
                this.isOrdersView = false;
                this.isCompletedView = true;
                this.isProcessingView = false;
                this.isInTransitView = false;
            },
            viewInTransit() {
                this.isOrdersView = false;
                this.isCompletedView = false;
                this.isProcessingView = false;
                this.isInTransitView = true;
            },
            viewProcessing() {
                this.isOrdersView = false;
                this.isCompletedView = false;
                this.isProcessingView = true;
                this.isInTransitView = false;
            },
            windowHistoryToDashboard() {
                window.history.replaceState({}, document.title, "/" + "dashboard");
            },
        },
    };
</script>
